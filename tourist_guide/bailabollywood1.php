	<h3 class="text-center"> 20 places to visit in New Delhi</h3><br>
	<p>Exploring the beautiful historical monuments of the Indian capital should be on any traveler's itinerary. Delhi is a city with a huge architectural heritage, with many iconic buildings, from forts to tombs, mosques and churches.</p>
