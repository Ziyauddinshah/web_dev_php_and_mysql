			<h3>Purana Qila (The Old Fort)</h3>
			<p>Although often overlooked, Purana Qila (Old Fort) is worth a visit as most tourists go directly to the famous Red Fort in Delhi. With a history spanning some 2,500 years, many of the impressive existing buildings date back to the 1500s, but there is evidence of earlier structures dating back to the 3rd century. In the affairs of the region, the current structure has for centuries played an important role and had a particular influence on the Muslim religion, as evidenced by buildings such as the Qila I-Kuna Mosque, a unique domed cult site built in 1541. The site is two square kilometers long and you can enjoy exploring its thick walls and three huge gates that are especially impressive all night long.<br><br>

            There is also a visit to the 200 acre Mehrauli Archaeological Park with numerous important structures, some of which date back more than 1000 years. The old remains of Lal Kot and more modern evidence of British rule under Queen Victoria are illuminated.</p>
