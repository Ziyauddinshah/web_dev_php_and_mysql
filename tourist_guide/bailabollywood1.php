			<p>The Handicraft Museum, officially called the National Museum of Handicrafts and Looms, New Delhi displays a wealth of traditional Indian handicrafts. Among its many fascinating features, highly skilled workers will display their centuries-old skills and view large collections of textiles, carpentry, and ceramics.<br><br>

The architecture is interesting, as well as the authentic mud huts and a Gujarati wooden holiday home, decorated with traditional art and decorated with exquisite woodcuts, paintings, papier-mâché and stickers.</p>
