            One of the most popular places linked to the Jama Masjid is Chandni Chowk, the massive main street of Old Delhi, the market area for shopping and food. Naya Bazaar and Gadodial are particularly interesting and famous spice markets where hundreds of products are displayed, including anise, ginger, different grains, saffron, lotus seeds, pickles and shuttles.</p>
