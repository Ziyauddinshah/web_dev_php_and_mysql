			<h3>India Gate</h3>
			<p>Like the famous Arc de Triomphe in Paris, the Indian Gate or Gateway of India is an impressive monument built to commemorate the Indian soldiers who were killed in the First World War. Beneath the massive structure, an eternal flame burns, and its walls are etched with the names of more than 90,000 soldiers who died in the conflict.<br><br>

            Standing on a red stone base with a shallow dome at the top, occasionally filled with oil (usually only during important birthdays), the structure overlooks the park, an area always busy with crowds of tourists and locals enjoying a picnic or just a relaxing time. One of the best experiences is to contemplate the Gateway of India at night, a totally free activity that will fill you with satisfaction; an unmissable postcard. </p>
