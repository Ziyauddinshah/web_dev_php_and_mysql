Other features include various unusual items such as a set of cars belonging to the influential Maharajas of the region, including the Maharaja teak car of Mysore, decorated with ivory; the home of the Prince of Wales, dating from 1876; and the retained ashes of Gandhi after his assassination in 1948. Another view shows the head of an elephant run over by a postal train in 1894. For children, it is fun to ride a nice narrow-gauge steam train.</p>
