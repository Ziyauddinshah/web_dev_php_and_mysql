			<h3>The Red Fort</h3>
			<p>Built by Shah Jahan in 1648, the beautiful Red Fort was until 1857 as the seat of Mughal power. This magnificent structure with its large walls of red sandstone covers an area of ​​more than two kilometers, all of which are shaped like a crown and a seabed. The impressive main entrance is called the Lahore Gate, which faces Lahore in Pakistan, while the even larger Delhi Gate was used by the emperor for ceremonial processions.</p>
			<p>Tourists arrive at the Lahore gate to access the Chhatta Chowk, a bazaar dating back to the 17th century, where items such as silk, gems and antiques can be purchased, along with souvenirs and typical food.</p>
			<p>A great way to see the Red Fort and other sights is to book a day trip to Old and New Delhi. A personal guide and driver will take you to points of particular interest, such as Humayun's Tomb, Qutub Minar, Jama Masjid, the Delhi Gate and the famous Lotus Temple, one of the high points of this almost 9-hour adventure traveling through the city.</p>

